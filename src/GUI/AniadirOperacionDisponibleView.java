    private void aniadirOperacionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aniadirOperacionBtnActionPerformed
        if(nombreOperacion.getText().isEmpty() || tiempoOperacion.getText().isEmpty() ){
             JOptionPane.showMessageDialog(null, "Debes completar los campos");
        }else{
            String cambioComa = tiempoOperacion.getText();
            cambioComa = cambioComa.replace(",", ".");
            if(Double.parseDouble(cambioComa)>0){
                            aux = new TipoOperacion(nombreOperacion.getText(),Double.parseDouble(cambioComa));
                            this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Introduzca cantidad valida o positiva");
            }

        }    
    }//GEN-LAST:event_aniadirOperacionBtnActionPerformed

