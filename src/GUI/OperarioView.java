    public void setOpererario(Operario op , String date, Empresa em){
        this.em = em;
        
        aniadeOpDiaria.setEnabled(false);
        actualizaTiempoDia.setEnabled(false);
        
        this.operario = op;
        this.date = date;
        this.fillOperacionesPanel(operario.getOperacionesDiarias(date).getListaOperaciones());
        this.name.setText(op.getNombre());
        DecimalFormat df = new DecimalFormat("###.##");
        this.porcDiario.setText(Double.toString(redondear(op.getPorcentajeDia(date), 2)) + "%");
        this.porcTotal.setText(Double.toString(redondear(op.getPorcentajeHistorico(), 2)) + "%");
        operacionCB.removeAllItems();
        tiempoDia.setText(Double.toString(op.getOperacionesDiarias(date).getTiempoRealizado()));
        fechaLabel.setText(date);
        
        if(!op.toString().equals("Operario inexistente")){
                    aniadeOpDiaria.setEnabled(true);
                    if(op.getOperacionesDiarias(date).totalOperaciones()>0){
                          actualizaTiempoDia.setEnabled(true);
                    }

        }
        
        
        ArrayList < TipoOperacion> aux = em.getOperacionesDisponibles().getArrayDis();
        
       for(TipoOperacion a : aux ){
            operacionCB.addItem(a.toString());
       }
       
       repaint();
        repaint();
        revalidate();
    }
    
    

    public double redondear(double numero,int digitos) {
        int cifras=(int) Math.pow(10,digitos);
        return Math.rint(numero*cifras)/cifras; 
    }
    
    private void fillOperacionesPanel (ArrayList<Operacion> aList) {
// Se elimina la información antigua, si la hubiera
        String aux = "<html> <tr> <td> Operacion </td> Tiempo <td> Repeticiones </td> </tr>";
        
        for (Operacion t : aList) {
                aux +="<tr><td>" + t.getTipoOperacion().getNombre() + "</td><td>" + t.getTipoOperacion().getTiempo() + "</td><td>" + t.getRepeticiones() + "</td></tr>";
        }
        
        aux += "</html>";
        muestraDia.setText(aux);
        repaint();
        revalidate();
    }

