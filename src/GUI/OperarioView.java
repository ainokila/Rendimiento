    private void aniadeOpDiariaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aniadeOpDiariaActionPerformed
       if(operario != null){
        int repeticionesS;
        String tipoOperacion ;
        TipoOperacion op;
        
        if (repeticiones.getText()!=null   ){
            repeticionesS = (int) Integer.parseInt(repeticiones.getText());
          
            tipoOperacion = (String) operacionCB.getSelectedItem();
            op = em.getOperacionesDisponibles().operacion(tipoOperacion);
            OperacionesDiarias aux = operario.getOperacionesDiarias(date);
            aux.insertar(new Operacion(op, repeticionesS));
            operario.setOperacionesDiarias(date, aux);
            this.setOpererario(operario, date, em);
            
            repaint();
            revalidate();
           
            
            
        }else{
           JOptionPane.showMessageDialog(null, "Introduzca un valor valido"); 
        }
        repeticiones.setText(null);
       }
        
    }//GEN-LAST:event_aniadeOpDiariaActionPerformed

    private void actualizaTiempoDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizaTiempoDiaActionPerformed
        if(operario != null){
            String cambioComa = tiempoDia.getText();
            cambioComa = cambioComa.replace(",", ".");
            if(Double.parseDouble(cambioComa)>0){
                         operario.getOperacionesDiarias(date).setTiempoRealizado(Double.parseDouble(cambioComa));
                         this.operario.calculaPorcHist();
                         this.setOpererario(operario, date, em);
            }else{
                JOptionPane.showMessageDialog(null, "Introduzca un tiempo valido"); 
            }

            
        }
 
    }//GEN-LAST:event_actualizaTiempoDiaActionPerformed

